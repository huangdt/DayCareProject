public void populateTable(){
    if(course.getClassroom()!=null){
        room.setText(course.getClassroom().getRoomNumber());

    }
    else{
        room.setText("No Assigned");

    }
    DefaultTableModel dtm1=(DefaultTableModel)notassign.getModel();
    int rowCount=notassign.getRowCount();
     for(int i=rowCount-1;i>=0;i--){
            dtm1.removeRow(i);
        }
       
    Rule r=course.getRule();
            for(Classroom cl:school.getClassroomlist())
            {
                if(course.getClassroom()==cl)
                    continue;
                 if(cl.getCourselist().size()==0){
               Object row[] = new Object[2];
            row[0]=cl;
            row[1]=""+cl.getCourselist().size()+"/"+course.getRule().getGroupRoom();
            dtm1.addRow(row);
                 }
                 else{
                 if(cl.getCourselist().get(0).getRule()==r)
                 if(r.getGroupRoom()>cl.getCourselist().size()){
                   Object row[] = new Object[2];
            row[0]=cl;
            row[1]=""+cl.getCourselist().size()+"/"+course.getRule().getGroupRoom();
            dtm1.addRow(row);
                     
                     }
                 }
                  
            }
  
    }
