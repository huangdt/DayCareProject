    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbTeacher.getSelectedRow();
        if (selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first to view details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            String s1=null,s2=null,s3=null;
            double d1=0;
            int i1=0;
            String sid = (String)tbTeacher.getValueAt(selectedRow, 0);
            for(Person s: school.getTeacherlist()){
                Teacher ss=(Teacher)s;
                if(ss.getId().equals(sid)){
                    s1=ss.getFirstName();
                    s2=ss.getLastName();
                    s3=ss.getId();
                    i1=ss.getAge();
                    d1=ss.getWage();
                    break;
                }}
                Teacher s=(Teacher)Teacher.getInstance(d1,s3,i1,s1,s2);
                ViewTeacher vsu = new ViewTeacher(school,container, s);
                container.add("ViewTeacher",vsu);
                CardLayout layout = (CardLayout) container.getLayout();
                layout.next(container);
            }
    }//GEN-LAST:event_btnViewActionPerformed

