    public void populateTable(){
        int rowCount = tbStudent.getRowCount();
        DefaultTableModel dtm=(DefaultTableModel)tbStudent.getModel();
        for(int i=rowCount-1;i>=0;i--){
            dtm.removeRow(i);
        }
       
        for(Person s:school.getStudentlist()){
            Student ss=(Student)s;
            Object row[] = new Object[5];
            row[0]=ss.getStudentId();
            row[1]=ss.getFirstName();
            row[2]=ss.getLastName();
            row[3]=ss.getAge();
            row[4]=ss.getRegistrationDate();
            dtm.addRow(row);
        }
    }
