public void populateTable(){
        int rowCount = assign.getRowCount();
        DefaultTableModel dtm=(DefaultTableModel)assign.getModel();
        for(int i=rowCount-1;i>=0;i--){
            dtm.removeRow(i);
        }
       
        for(Person p:course.getStudentlist()){
            Object row[] = new Object[2];
            row[0]=p;
            row[1]=p.getAge();
            dtm.addRow(row);
        }
        
          int rowCount1 = notassign.getRowCount();
        DefaultTableModel dtm1=(DefaultTableModel)notassign.getModel();
        for(int i=rowCount1-1;i>=0;i--){
            dtm1.removeRow(i);
        }
       
       
           
       Rule r=course.getRule();
            for(Person p:school.getStudentlist())
            {
                int sym=0;
                 if(p.getAge()>=r.getStartAge()&&p.getAge()<r.getEndAge()){
                     for(Course c:school.getCourselist())
                     if(c.getStudentlist().contains(p)){
                         sym=1;
                     break;
                     }
               if(sym==0){
                 Object row[] = new Object[2];
                      row[0]=p;
            row[1]=p.getAge();
            dtm1.addRow(row);
               }
                 }
                     
            }
       if(course.getStudentlist().size()==course.getRule().getGroupSize()){
           jButton2.setEnabled(false);
       }else{
           jButton2.setEnabled(true);
       }
    }
