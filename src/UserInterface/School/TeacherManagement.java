    public void populateTable(){
        int rowCount = tbTeacher.getRowCount();
        DefaultTableModel dtm=(DefaultTableModel)tbTeacher.getModel();
        for(int i=rowCount-1;i>=0;i--){
            dtm.removeRow(i);
        }
       
        for(Person s:school.getTeacherlist()){
            Teacher ss=(Teacher)s;
            Object row[] = new Object[4];
            row[0]=ss.getId();
            row[1]=ss.getFirstName();
            row[2]=ss.getLastName();
            row[3]=ss.getAge();
            dtm.addRow(row);
        }
    }
