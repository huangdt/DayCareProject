    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbStudent.getSelectedRow();
        if (selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first to view details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            String s1=null,s2=null,s3=null,s4=null,s5=null,s6=null,s7=null,s8=null;
            double d1=0;
            int i1=0,i2=0;
            String sid = (String)tbStudent.getValueAt(selectedRow, 0);
            for(Person s: school.getStudentlist()){
                Student ss=(Student)s;
            if(ss.getStudentId().equals(sid)){
                 s1=ss.getFatherName();
                 s2=ss.getMotherName();
                 s3=ss.getRegistrationDate();
                 s4=ss.getStudentId();
                 d1=ss.getGpa();
                 s5=ss.getBirthday();
                 i1=0;
                 s6=ss.getId();
                 i2=ss.getAge();
                 s7=ss.getFirstName();
                 s8=ss.getLastName();
                 break;
            }}
            Student s=(Student)Student.getInstance(s1,s2,s3,s4,d1, s5,i1,s6,i2,s7,s8);
            ViewStudent vsf = new ViewStudent(school,container, s);
            container.add("ViewStudent",vsf);
            CardLayout layout = (CardLayout) container.getLayout();
            layout.next(container);
        }
    }//GEN-LAST:event_btnViewActionPerformed

