    public void populateTable(){
        int rowCount = tbRule.getRowCount();
        DefaultTableModel dtm=(DefaultTableModel)tbRule.getModel();
        for(int i=rowCount-1;i>=0;i--){
            dtm.removeRow(i);
        }
       
        for(Rule s:school.getRulelist()){
            Object row[] = new Object[5];
            row[0]=s.getRuleId();
            row[1]=s.getGroupSize();
            row[2]=s.getStartAge();
            row[3]=s.getEndAge();
            row[4]=s.getGroupRoom();
            dtm.addRow(row);
        }
    }

