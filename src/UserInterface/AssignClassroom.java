    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = notassign.getSelectedRow();
        if(selectedRow >=0 ){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete this student?", "Warning",dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
                Classroom s = (Classroom) notassign.getValueAt(selectedRow, 0);
                if(course.getClassroom()!=null){
                    for(Classroom cl:school.getClassroomlist())
                        if(cl.getCourselist().contains(course)){
                            cl.getCourselist().remove(course);
                            break;
                        }
//s.getCourselist().remove(course);
                }
                course.setClassroom(s);

                s.addCourse(course);
                
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row from the table first","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

