    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbRule.getSelectedRow();
        if(selectedRow >=0 ){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete this rule?", "Warning",dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
                String ru = (String) tbRule.getValueAt(selectedRow, 0);
//                for(Rule r: school.getRulelist())
//                  if(r.getRuleId().equals(ru)){
//                  school.getRulelist().remove(r);
//                  break;
//                
//              }
            school.removeRule(ru);
            school.setRuleTime(new Date());
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row from the table first","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

